        lines.extend([f"- {shape[0]}x{shape[1]} ({count})" for shape, count in top_img_counts])
        lines.append("Top 5 mask shapes:")
        lines.extend([f"- {shape[0]}x{shape[1]} ({count})" for shape, count in top_mask_counts])
        lines += [
            "Image size statistics:",
            f"- Mean height: {metrics['mean_height']:.2f}",
            f"- Mean width: {metrics['mean_width']:.2f}",
            f"- Min height: {metrics['min_height']:.2f}",
            f"- Max height: {metrics['max_height']:.2f}",
            f"- Min width: {metrics['min_width']:.2f}",
            f"- Max width: {metrics['max_width']:.2f}",
            "Mask size statistics:",
            f"- Mean height: {metrics['mean_mask_height']:.2f}",
            f"- Mean width: {metrics['mean_mask_width']:.2f}",
            f"- Min height: {metrics['min_mask_height']:.2f}",
            f"- Max height: {metrics['max_mask_height']:.2f}",
            f"- Min width: {metrics['min_mask_width']:.2f}",
            f"- Max width: {metrics['max_mask_width']:.2f}",
        ]
