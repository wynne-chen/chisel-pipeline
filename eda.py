    #--- Spatial Analysis Methods --- #
    def compute_spatial_metadata(self):
        '''
        Computes and returns spatial metadata of the dataset.
        
        Returns: 
            list of SpatialMetadata objects.
        '''
        if self.spatial_mode == 'dataset_level':
            n = len(self.image_paths)
            base = self.spatial_metadata
            return [base for _ in range(n)]
        
        if self.stage == 'post':
            self.spatial_metadata = self.spatial_metadata_service.get_spatial_metadata_from_csv(self.spatial_metadata_csv_path)
            return self.spatial_metadata
        
        try:
            self.spatial_metadata = [self.load_spatial_metadata(i) for i in range(len(self.image_paths))]
            return self.spatial_metadata
        except Exception:
            self.logger.exception("Failed to compute spatial metadata.")
            return [None] * len(self.image_paths)

    def _summarise_spatial_metadata(
        self,
        spatial_metadata: list[SpatialMetadata],
        center_lat: float,
        center_lon: float,
        minx: float,
        miny: float,
        maxx: float,
        maxy: float,
        zoom_level: float,
        max_countries: int = 5,
        max_crs: int = 3,
    ) -> list[str]:

        valid = [sm for sm in spatial_metadata if sm is not None]
        n_total = len(spatial_metadata)
        n_valid = len(valid)
        with_bounds = [sm for sm in valid if sm.bounds is not None]
        with_centroid_wgs84 = [sm for sm in valid if sm.centroid_wgs84 is not None]
        
        crs_counts: dict[str, int] = {}
        for sm in valid:
            if sm.epsg is not None:
                key = f"EPSG:{sm.epsg}"
            elif sm.crs_wkt:
                try:
                    key = CRS.from_wkt(sm.crs_wkt).to_string()
                except Exception:
                    key = "WKT (unparsed)"
            else:
                continue
            crs_counts[key] = crs_counts.get(key, 0) + 1
        top_crs = sorted(crs_counts.items(), key=lambda kv: kv[1], reverse=True)[:max_crs]
        country_counts: dict[str, int] = {}
        for sm in valid:
            if sm.country:
                country_counts[sm.country] = country_counts.get(sm.country, 0) + 1
        top_countries = sorted(country_counts.items(), key=lambda kv: kv[1], reverse=True)[:max_countries]
        mean_cm_values = [sm.mean_cm_per_pixel for sm in valid if sm.mean_cm_per_pixel is not None]
        if mean_cm_values:
            mean_cm_values_sorted = sorted(mean_cm_values)
            n = len(mean_cm_values_sorted)
            median_cm = (
                mean_cm_values_sorted[n // 2]
                if n % 2 == 1
                else (mean_cm_values_sorted[n // 2 - 1] + mean_cm_values_sorted[n // 2]) / 2
            )
            cm_line = (
                f"Mean cm/pixel: min={min(mean_cm_values_sorted):.2f}, "
                f"med={median_cm:.2f}, max={max(mean_cm_values_sorted):.2f}"
            )
        else:
            cm_line = "Mean cm/pixel: unavailable"
        if top_crs:
            crs_line = "Top CRS: " + ", ".join([f"{crs} ({count})" for crs, count in top_crs])
        else:
            crs_line = "Top CRS: unavailable"
        if top_countries:
            country_line = "Top countries: " + ", ".join([f"{country} ({count})" for country, count in top_countries])
        else:
            country_line = "Top countries: unavailable"
        lines = [
            f"Records: {n_valid}/{n_total} valid",
            f"Bounds (WGS84): min({minx:.5f}, {miny:.5f}) max({maxx:.5f}, {maxy:.5f})",
            f"Map center (lat, lon): ({center_lat:.5f}, {center_lon:.5f}), zoom={zoom_level:.2f}",
            f"With bounds: {len(with_bounds)} | With centroid_wgs84: {len(with_centroid_wgs84)}",
            crs_line,
            cm_line,
            country_line,
        ]
        return lines
    
