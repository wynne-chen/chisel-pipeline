    def _build_section_spatial_metadata(self, context: dict, idx: int, include_in_pdf: bool = True, include_images: bool = True, include_text: bool = True, show_inline_override: bool | None = None) -> ReportSection:
        '''Builds a report section for the spatial metadata.'''

        if context['spatial_metadata'] is None:
            context['spatial_metadata'] = self.compute_spatial_metadata()

        country_dist_fig = self.plot_country_distribution(context['spatial_metadata'])

        if context['spatial_layer'] is None:
            context['spatial_layer'] = self.get_layer_source(context['spatial_metadata'])
        
        figures = []
        text_lines = []

        mpl_fig = None 

        if context['spatial_layer'] is not None:
            _, center_lat, center_lon, minx, miny, maxx, maxy = context["spatial_layer"]
            if context["zoom_level"] is None:
                context["zoom_level"] = self.calc_zoom_for_map(minx, miny, maxx, maxy)
            if context["bounds_area"] is None:
                context["bounds_area"] = self.get_bounds_area(minx, miny, maxx, maxy)

            if context["bounds_area"] is None:
                self.logger.warning("Bounds area is None; skipping map plot.")
            elif context["bounds_area"] > self.config.get('max_bounds_area', 500_000):
                self.logger.warning("Union extent exceeds max bounds area; skipping map plot.")
            else:
                a4_aspect = WriteReport.A4_LANDSCAPE_SIZE[0] / WriteReport.A4_LANDSCAPE_SIZE[1]
                padded_bounds = self._pad_bounds_to_aspect(minx, miny, maxx, maxy, a4_aspect)
                go_fig = self.plot_spatial_metadata(context['spatial_layer'][0], context['spatial_layer'][1], context['spatial_layer'][2], context['zoom_level'], context['bounds_area'], padded_bounds)
                if go_fig is not None:
                    try:
                        img_bytes = go_fig.to_image(format="png", scale=1)
                        pil_img = Image.open(io.BytesIO(img_bytes)).convert("RGB")
                        mpl_fig, ax = plt.subplots(
                            figsize=(WriteReport.A4_LANDSCAPE_SIZE)
                        )
                        ax.imshow(pil_img)
                        ax.axis("off")
                        mpl_fig.tight_layout(pad=0)
                    except Exception:
                        self.logger.exception("Failed to rasterise spatial metadata map to Matplotlib figure")
                else:
                    text_lines.append("Failed to plot spatial metadata map.")
        else:
            text_lines.append("Spatial metadata map: unavailable (no valid CRS/bounds for any record).")


        if include_text:
            if context["spatial_layer"] is not None and mpl_fig is not None:
                _, center_lat, center_lon, minx, miny, maxx, maxy = context["spatial_layer"]
                text_lines.extend(self._summarise_spatial_metadata(
                    spatial_metadata=context["spatial_metadata"],
                    center_lat=center_lat,
                    center_lon=center_lon,
                    minx=minx,
                    miny=miny,
                    maxx=maxx,
                    maxy=maxy,
                    zoom_level=context["zoom_level"],
                ))
            else:
                text_lines.extend(self._summarise_spatial_metadata_no_map(context["spatial_metadata"]))
        
        if self.spatial_mode == "dataset_level":
            text_lines.append("Spatial metadata provenance: dataset-level user input (coarse).")
        
        if mpl_fig is not None:
            figures.append(mpl_fig)
        if country_dist_fig is not None:
            figures.append(country_dist_fig)


        sec = ReportSection(
            idx=idx, 
            slug='spatial_metadata', 
            title='Spatial Metadata', 
            text_lines=text_lines,
            figures=figures,
            include_in_pdf=include_in_pdf, 
            include_images=include_images, 
            include_text=include_text, 
            show_inline_override=show_inline_override)
        return sec
    
