    def _build_section_object_statistics_per_class(self, context: dict, idx: int, class_id: int, include_in_pdf: bool = True, include_images: bool = True, include_text: bool = True, show_inline_override: bool | None = None) -> ReportSection:

        eligible = self._eligible_sample_indices_for_class(context, class_id)
        obj_stats = context["obj_stats_by_class"].get(class_id, None)
        if obj_stats is None:
            filtered_for_stats = []
            for i in eligible:
                mask = self.load_mask(i)
                if mask is not None:
                    filtered_for_stats.append(self.image_processor.filter_mask_by_class_id(mask, class_id, self.set_name))
            obj_stats = self.compute_object_stats(filtered_masks=filtered_for_stats)
            context["obj_stats_by_class"][class_id] = obj_stats

        if not eligible:
            random_idx = np.random.choice(self.sample_indices)
        else:
            random_idx = np.random.choice(eligible)
        
        raw_mask = self.load_mask(random_idx)
        filtered_mask = self.image_processor.filter_mask_by_class_id(raw_mask, class_id, self.set_name)

        figures = []
        for fig in [
            self.plot_object_area_distribution(obj_stats.get("areas", [])),
            self.plot_object_centroids(obj_stats.get("centroids", []), obj_stats.get("image_shapes", None)),
            self.plot_object_count_distribution(obj_stats.get("counts", [])),
            self.plot_centre_bias_distribution(obj_stats.get("centre_bias", [])),
            self.plot_edge_touching_distribution(obj_stats.get("edge_touching", [])),
            self.plot_pixel_heatmap(obj_stats.get("centroid_heatmap")),
            self.visualiser.return_image_mask_pair(self.load_image(random_idx), filtered_mask),
            ]:
            if fig is not None:
                figures.append(fig)
        
        counts = np.asarray(obj_stats.get("counts", []), dtype=np.int64)
        area_ratios = np.asarray(obj_stats.get("area_ratios", []), dtype=np.float64)
        metrics = {
            "class_id": class_id,
            "num_images_analysed": int(len(counts)),
            "images_with_objects": int((counts > 0).sum()) if counts.size else 0,
            "total_objects": int(counts.sum()) if counts.size else 0,
            "mean_objects_per_image": float(counts.mean()) if counts.size else 0.0,
            "mean_area_ratio": float(area_ratios.mean()) if area_ratios.size else 0.0,
        }

        sec = ReportSection(
            idx=idx,
            slug=f"object_statistics_{class_id}",
            title=f"Object Statistics for Class {class_id}",
            metrics=metrics,
            figures=figures,
            include_in_pdf=include_in_pdf,
            include_images=include_images,
            include_text=include_text,
            show_inline_override=show_inline_override
        )
        return sec

