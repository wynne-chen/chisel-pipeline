'''
This class is used to perform exploratory data analysis on a dataset.
It is initialised with a directory containing images and masks.
Consists of methods for:
    - loading images and masks
    - analysis 
    - visualisation of analysis results

A full EDA report is generated by running the report() method.
'''

class DatasetEDA:
    def __init__(self, data_dir, class_id, set_name, config=None, stage='pre', spatial_metadata=None):
        '''
        DatasetEDA is initialised with a directory containing images and masks. It uses FileHandler to load and filter the dataset.
        Args:
            data_dir (str): Path to the directory containing the dataset (images, masks, etc.)
            class_id (int): Class ID for mask filtering.
            set_name (str): Set name for mask filtering.
            config (dict): Configuration dictionary for FileHandler and processing.
            stage (str): 'pre' for pre-processing, 'post' for post-processing. Used for labeling/reporting and for spatial metadata loading. Default is 'pre'.
            spatial_metadata (SpatialMetadata): [Optional] ]Spatial metadata object for user input, if the dataset has no actual spatial metadata.
            sample_size (int): Number of images/masks to sample for EDA, particularly for methods that are more compute intensive.
        '''
        self.config = config or {}
        self.data_dir = data_dir
        self.class_id = class_id
        self.set_name = set_name
        self.file_handler = FileHandler(self.config)
        self.image_processor = ImageProcessor(self.config)
        self.spatial_metadata_service = SpatialMetadataService(self.config)
        self.annotation_generator = AnnotationGenerator(self.config)
        self.visualiser = Visualiser(self.config)
        self.plotter = EDAPlotter(config=self.config, top_k=self.top_k if hasattr(self, "top_k") else 10, set_name=set_name, logger=logging.getLogger(__name__))
        self.sections_builder = EDASections(self)
        self.image_paths, self.mask_paths = self.file_handler.load_dataset(data_dir)       
        self.sample_size = min(self.config.get('sample_size', 50), len(self.image_paths), len(self.mask_paths)) 
        self.top_k = self.config.get('top_k', 10)
        self.plotter.top_k = self.top_k
        self.stage = stage

        if self.stage == 'post':
            self.spatial_metadata_csv_path = os.path.join(data_dir, 'dataset_file_directory.csv')

        if spatial_metadata is not None:
            self.spatial_mode = 'dataset_level'
            self.spatial_metadata = spatial_metadata
        else:
            self.spatial_mode = 'per_image'

        # fix sample indices for methods that require sampling
        np.random.seed(self.config.get('random_seed', 42))  

        self.sample_indices = np.random.choice(len(self.image_paths), self.sample_size, replace=False)

        self.image_shapes_counts = None
        self.mask_shapes_counts = None
        
        self.only_one_shape = True 
        self.only_one_class = True 
        self.image_mask_size_match = False 
        self.classes = class_id #assumes single class as default --> will be overwritten by unique_mask_values method if it is multiclass

        self.logger = logging.getLogger(__name__)
        self.logger.setLevel(logging.ERROR)
        self.index_analysis = EDAIndexAnalysis(config=self.config, logger=self.logger)

    def load_image(self, i):
        if i >= len(self.image_paths):
            self.logger.error(f"Image index out of range: {i} is greater than the number of images in the dataset: {len(self.image_paths)}")
            return None
        return self.image_processor.load_image(self.image_paths[i])
    
    def load_spatial_metadata(self, i):
        if i >= len(self.image_paths):
            self.logger.error(f"Image index out of range: {i} is greater than the number of images in the dataset: {len(self.image_paths)}")
            return None
        return self.spatial_metadata_service.get_spatial_metadata(self.image_paths[i])


    def load_mask(self, i):
        if i >= len(self.mask_paths):
            self.logger.error(f"Mask index out of range: {i} is greater than the number of masks in the dataset: {len(self.mask_paths)}")
            return None
        return self.image_processor.load_mask(self.mask_paths[i])
    
    def valid_image(self, image):
        return image.ndim == 3 and image.shape[2] == 3
    
    def ensure_shapes_loaded(self):
        '''
        Iterate through all images and masks to:
        - get all existing shapes in dataset
        - set only_one_shape and ref_shape
        - set image_mask_size_match to True if all shapes are the same; False if shapes are not the same
        - set ref_shape to the max height and width of the shapes if more than one shape is found; (0, 0) if no shapes are found
        - set image_shapes_counts and mask_shapes_counts to the counts of the shapes; {(0,0): 1} if no shapes are found
        '''
        if self.image_shapes_counts is not None:
            self.logger.info("Shapes already loaded. Returning.")
            return
        image_shapes = []
        mask_shapes = []

        if len(self.image_paths) == 0:
            self.logger.warning("No image files detected. Unable to compute metrics. Setting reference shape to (0, 0) and image shapes count to {(0,0): 1}.")
            self.ref_shape = (0, 0)
            self.image_shapes_counts = {(0,0): 1}
            return
        if len(self.mask_paths) == 0:
            self.logger.warning("No mask files detected. Unable to compute metrics. Setting reference shape to (0, 0) and mask shapes count to {(0,0): 1}.")
            self.ref_shape = (0, 0)
            self.mask_shapes_counts = {(0,0): 1}
            return

        for i in range(len(self.image_paths)):
            try:
                img = self.load_image(i)
                mask = self.load_mask(i)
                image_shapes.append(img.shape if img is not None else (0,0))
                mask_shapes.append(mask.shape if mask is not None else (0,0))

                del img
                del mask

            except Exception as e:
                self.logger.warning(f"Failed to load pair {i}: {self.image_paths[i]}, {self.mask_paths[i]}. Error: {e}")
                image_shapes.append((0,0))
                mask_shapes.append((0,0))

        self.image_shapes_counts = dict(Counter(image_shapes))
        self.mask_shapes_counts = dict(Counter(mask_shapes))

        if (len(self.image_shapes_counts) == 1 and next(iter(self.image_shapes_counts.keys())) == (0,0)):
            self.ref_shape = (0, 0)
            self.logger.warning("No image shapes detected. Unable to compute metrics. Returning empty dictionary and setting reference shape to (0, 0).")
            return {}
        if len(self.mask_shapes_counts) == 0:
            self.ref_shape = (0, 0)
            self.logger.warning("No mask shapes detected. Unable to compute metrics. Returning empty dictionary and setting reference shape to (0, 0).")
            return {}

        if len(self.image_shapes_counts) == 1 and len(self.mask_shapes_counts) == 1:
            self.ref_shape = next(iter(self.image_shapes_counts.keys()))[:2]
            self.logger.info(f"All images and masks have the same shape. Reference shape set to: {self.ref_shape}")
        else:
            self.only_one_shape = False
            all_shapes = [shape for shape in self.image_shapes_counts.keys() if shape is not None]
            if list(self.image_shapes_counts.keys()) == list(self.mask_shapes_counts.keys()): # since images and masks are loaded in pairs, we shouldn't need to sort the lists. if they don't match it means the image/mask pairs don't match, even if the overall lists of shapes are the same.
                self.image_mask_size_match = True
            else:
                self.image_mask_size_match = False #although it is false by default, declare explicitly for safety
                self.logger.warning(f"Image and mask shapes are not uniform. Image shape set: {self.image_shapes_counts.keys()}. Mask shape set: {self.mask_shapes_counts.keys()}")
                all_shapes.extend([shape for shape in self.mask_shapes_counts.keys() if shape is not None])
            max_height = max(shape[0] for shape in all_shapes)
            max_width = max(shape[1] for shape in all_shapes)
            self.ref_shape = (max_height, max_width)
            self.logger.info(f"Images and masks have more than one shape. Total number of image shapes is {len(self.image_shapes_counts)}. Total number of mask shapes is {len(self.mask_shapes_counts)}. Reference shape set to: {self.ref_shape}")


    # --- General Analysis Methods --- #
    def compute_basic_metrics(self):
        '''
        Computes and returns basic dataset metrics such as:
        - number of image and mask files/tiles
        - size of image and mask files/tiles
        - basic statistics for image height and width (mean, min, max)
        
        Returns: dict of nums and dicts
        '''
        self.ensure_shapes_loaded()

        metrics = {}

        metrics['image_count']= len(self.image_paths)
        metrics['mask_count'] = len(self.mask_paths)

        metrics['image_shapes'] = self.image_shapes_counts
        metrics['mask_shapes'] = self.mask_shapes_counts

        weighted_h = weighted_w = total = 0
        heights = []
        widths = []

        for shape, count in self.image_shapes_counts.items():
            if shape is None:
                continue
            h, w = shape[0], shape[1]
            weighted_h += h * count
            weighted_w += w * count
            total += count
            heights.append(h)
            widths.append(w)
        if total > 0:
            metrics['mean_height'] = weighted_h / total
            metrics['mean_width'] = weighted_w / total
        else:
            metrics['mean_height'] = metrics['mean_width'] = np.nan

        metrics['min_height'] = np.min(heights) if heights else np.nan
        metrics['max_height'] = np.max(heights) if heights else np.nan
        metrics['min_width'] = np.min(widths) if widths else np.nan
        metrics['max_width'] = np.max(widths) if widths else np.nan
        
        weighted_mh = weighted_mw = total_m = 0
        mask_heights = []
        mask_widths = []

        for shape, count in self.mask_shapes_counts.items():
            if shape is None:
                continue
            h, w = shape[0], shape[1]
            weighted_mh += h * count
            weighted_mw += w * count
            total_m += count
            mask_heights.append(h)
            mask_widths.append(w)
        
        if total_m > 0:
            metrics['mean_mask_height'] = weighted_mh / total_m
            metrics['mean_mask_width'] = weighted_mw / total_m
        else:
            metrics['mean_mask_height'] = metrics['mean_mask_width'] = np.nan
        metrics['min_mask_height'] = np.min(mask_heights) if mask_heights else np.nan
        metrics['max_mask_height'] = np.max(mask_heights) if mask_heights else np.nan
        metrics['min_mask_width'] = np.min(mask_widths) if mask_widths else np.nan
        metrics['max_mask_width'] = np.max(mask_widths) if mask_widths else np.nan

        return metrics

