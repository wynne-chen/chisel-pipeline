    def build_sections(self, context: dict | None = None) -> list[ReportSection]:
        """Build all report sections in deterministic order."""
        context = context or EDAContext().as_dict()
        return self.sections_builder.build_sections(context)

    def report(
        self, 
        show_inline: bool = False,
        save_pdf: bool = True,
        save_images: bool = True,
        save_summary: bool = True,
        output_dir: str | None = None):
        '''
        Runs a full EDA report: computes metrics, saves summaries, and shows key plots. Produces a PDF report and a summary JSON file.
        While it is possible to both show the report inline AND save the PDF, it is not recommended to do both. 
        Args:
            stage (str): 'pre' for pre-processing, 'post' for post-processing. Used for labeling/reporting.
            show_inline (bool): Whether to show plots inline.
            save_pdf (bool): Whether to save the PDF report.
            save_images (bool): Whether to save the images.
            save_summary (bool): Whether to save the summary JSON file.
            output_dir (str): The directory to save the report and artefacts.
        '''
        stage = self.stage
        base_dir = output_dir or "reports"
        artefacts = ReportArtefactManager(base_dir=base_dir, stage=stage, set_name=self.set_name)
        writer = WriteReport(artefacts=artefacts, set_name=self.set_name)
        context = EDAContext().as_dict()
        sections: list[ReportSection] = []

        if show_inline:
            print(f"\n{'='*40}\nDATASET EDA REPORT ({'PRE' if stage=='pre' else 'POST'}-PROCESSING)\n{'='*40}")


        sections = self.build_sections(context=context)

        # save report
        writer.render_report(sections, show_inline=show_inline, save_pdf=save_pdf, save_images=save_images, save_summary=save_summary)

        if show_inline:
            print(f"\n{'='*40}\nEND OF REPORT\n{'='*40}")
        any_save = save_pdf or save_images or save_summary
        return {
             "stage": stage,
             "run_dir": str(artefacts.run_dir) if any_save else None,
             "pdf_path": str(artefacts.pdf_path) if save_pdf else None,
             "summary_path": str(artefacts.summary_path) if save_summary else None,
             "num_sections": len(sections)
            }