    def _is_background_class(self, class_value) -> bool:
        return class_value == 0 or (
            isinstance(class_value, (tuple, list, np.ndarray)) and all(v == 0 for v in class_value)
        )

    def _get_foreground_classes(self, context: dict) -> list:
        if context["foreground_classes"] is None:
            context["foreground_classes"] = [c for c in self.classes if not self._is_background_class(c)]
        return context["foreground_classes"]

    def _get_cached_average_image(self, context: dict):
        if context["avg_img"] is None:
            context["avg_img"] = self.compute_average_image()
        return context["avg_img"]


    def _eligible_sample_indices_for_class(self, context: dict, class_id: int) -> list[int]:
        if not context.get("_eligible_warmed"):
            foreground_classes = self._get_foreground_classes(context)
            d = {c: [] for c in foreground_classes}
            for i in self.sample_indices:
                m = self.load_mask(i)
                if m is None:
                    continue
                p = set(np.unique(m).tolist())
                for c in foreground_classes:
                    if c in p:
                        d[c].append(i)
            context["eligible_sample_indices_by_class"] = d
            context["_eligible_warmed"] = True
        return list(context["eligible_sample_indices_by_class"].get(class_id, []))
            
            

    def _compute_multiclass_summary(self, context: dict):
        if context["multiclass_summary"] is not None:
            return context["multiclass_summary"]

        foreground_classes = self._get_foreground_classes(context)

        class_ids, image_counts, object_counts, total_areas, area_ratios = [], [], [], [], []

        for class_id in foreground_classes:
            if class_id not in context["obj_stats_by_class"]:
                eligible = self._eligible_sample_indices_for_class(context, class_id)
                filtered_masks = []
                for i in eligible:
                    mask = self.load_mask(i)
                    if mask is not None:
                        filtered_masks.append(self.image_processor.filter_mask_by_class_id(mask, class_id, self.set_name))
                context["obj_stats_by_class"][class_id] = self.compute_object_stats(filtered_masks=filtered_masks)

            s = context["obj_stats_by_class"][class_id]
            counts = np.asarray(s.get("counts", []), dtype=np.float64)
            totals = np.asarray(s.get("total_areas", []), dtype=np.float64)
            ratios = np.asarray(s.get("area_ratios", []), dtype=np.float64)

            class_ids.append(class_id)
            image_counts.append(int((counts > 0).sum()))
            object_counts.append(int(counts.sum()))
            total_areas.append(float(totals.sum()))
            area_ratios.append(float(ratios.mean()) if ratios.size else 0.0)

        context["multiclass_summary"] = (class_ids, image_counts, object_counts, total_areas, area_ratios)
        return context["multiclass_summary"]


