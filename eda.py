    # --- Image Analysis Methods --- #
    def compute_colour_distribution(self):
        '''
        Computes unique colours, dominant colours, and colour distribution statistics for the dataset based on strided pixels.
        Pixel stride varies according to image size. Target pixels will be set to 512x512=262144 pixels.

        Note: Colour channels are in BGR order (OpenCV convention).
        
        By default, takes only a sample of the dataset which is fixed in the config.

        Args:
            top_k (int): number of top colours to store. Default is 10.
        
        Returns:
            dict: Contains unique colours, colour counts, dominant colour, average colour (BGR), and colour distribution percentages.
        '''
        if len(self.image_paths) == 0:
            self.logger.warning("No image files detected. Unable to compute colour distribution. Returning empty dictionary.")
            return {}

        colour_counter = Counter()
        sum_pixels = np.zeros(3)
        total_pixels = 0

        for i in self.sample_indices:
            image = self.load_image(i)

            if self.valid_image(image):
                stride = max(1, int(np.sqrt(image.shape[0] * image.shape[1] / 262144))) # see docstring above. target for stride of 1 is 512x512=262144 pixels.
                pixels = image[::stride, ::stride, :].reshape(-1, 3)

                colour_counter.update(map(tuple, pixels))

                sum_pixels += pixels.sum(axis=0)
                total_pixels += pixels.shape[0]

            else:
                self.logger.warning(f"Image {i} is not a 3-channel image. Skipping.")
                continue
        
        if total_pixels == 0 or not colour_counter:
            self.logger.warning("No valid 3-channel images for colour distribution.")
            return {}
        
        dominant_colour = max(colour_counter, key=colour_counter.get)           # note that in mixed-sized datasets with a big range of image sizes, this will be biased towards larger images, but it is meaningful to see the dominant colour in the overall dataset.
        average_colour = tuple((sum_pixels / total_pixels).astype(int))
        colour_distribution = {
            colour: (count / total_pixels * 100) 
            for colour, count in colour_counter.items()
            }

        colour_dict = {
            'unique_colours': colour_counter.most_common(self.top_k),
            'dominant_colour': dominant_colour,
            'average_colour_bgr': average_colour,
            'colour_distribution_percentages': colour_distribution,
            'total_unique_colours': len(colour_counter),
            'total_pixels_analysed': total_pixels
        }
        
        return colour_dict 

    @staticmethod
    def _hist_percentiles(counts, bin_edges, qs=(1, 25, 50, 75, 99)):
        cdf = np.cumsum(counts)
        if cdf[-1] == 0:
            return np.array([np.nan] * len(qs))
        targets = np.array(qs) / 100.0 * cdf[-1]
        idxs = np.searchsorted(cdf, targets, side="left")
        idxs = np.clip(idxs, 0, len(bin_edges) - 2)
        return bin_edges[idxs]

    def compute_intensity_histogram(self):
        '''
        Computes and returns colour/brightness distribution statistics for the dataset.
        Histogram data is a tuple (counts, bin_edges) as returned by np.histogram.
        Args:
            sample_size : int, number of images to sample. None by default (uses entire dataset. Use if dataset is too huge.
        Returns: 
            intensity_distribution:  dictionary of min/max/mean, histogram data, etc. for both overall and per colour channel values.
        '''
        if len(self.image_paths) == 0:
            self.logger.warning("No image files detected. Unable to compute intensity histogram. Returning empty dictionary.")
            return {}
        
        channel_dict = {"B": 0, "G": 1, "R": 2}
            
        intensity_distribution = {}
        bin_edges = np.linspace(0, 255, 51)

        all_channel_stats = {
            ch: {
                'sum': 0,
                'sum_sq': 0,
                'count': 0,
                'min': 255,
                'max': 0,
                'hist': np.zeros(50, dtype=np.float64)
            } for ch in channel_dict
        }
        grey_stats = {
            'sum': 0,
            'sum_sq': 0,
            'count': 0,
            'min': 255,
            'max': 0,
            'hist': np.zeros(50, dtype=np.float64)
        }

        for i in self.sample_indices:
            image = self.load_image(i)
            if not self.valid_image(image):
                self.logger.warning(f"Image {i} is not a valid image. Skipping.")
                continue
            pixels = image.reshape(-1, 3)
            b = pixels[:, 0]
            g = pixels[:, 1]
            r = pixels[:, 2]
            for channel, arr in (("B", b), ("G", g), ("R", r)):
                vals = arr.astype(np.float64, copy=False)
                all_channel_stats[channel]["sum"] += float(vals.sum())
                all_channel_stats[channel]["sum_sq"] += float(np.square(vals, dtype=np.float64).sum())
                all_channel_stats[channel]["count"] += vals.size
                all_channel_stats[channel]["min"] = min(all_channel_stats[channel]["min"], float(vals.min()))
                all_channel_stats[channel]["max"] = max(all_channel_stats[channel]["max"], float(vals.max()))
                all_channel_stats[channel]["hist"] += np.histogram(vals, bins=bin_edges)[0]
            
            grey = cv2.cvtColor(image.astype(np.float32, copy=False), cv2.COLOR_BGR2GRAY).ravel()
            grey_float = grey.astype(np.float64)
            grey_stats['sum'] += grey_float.sum()
            grey_stats['sum_sq'] += (grey_float**2).sum()
            grey_stats['count'] += grey_float.size
            grey_stats['min'] = min(grey_stats['min'], grey_float.min())
            grey_stats['max'] = max(grey_stats['max'], grey_float.max())
            grey_stats['hist'] += np.histogram(grey_float, bins=bin_edges)[0]
        
        if grey_stats['count'] == 0:
            self.logger.warning("No valid 3-channel images for intensity histogram.")
            return {}

        for channel in channel_dict:
            channel_data = all_channel_stats[channel]
            intensity_distribution[f'{channel}_mean_intensity'] = channel_data['sum'] / channel_data['count']
            intensity_distribution[f'{channel}_std_intensity'] = np.sqrt(channel_data['sum_sq'] / channel_data['count'] - intensity_distribution[f'{channel}_mean_intensity']**2)
            intensity_distribution[f'{channel}_min_intensity'] = channel_data['min']
            intensity_distribution[f'{channel}_max_intensity'] = channel_data['max']
            intensity_distribution[f'{channel}_percentiles'] = self._hist_percentiles(channel_data['hist'], bin_edges)
            intensity_distribution[f'{channel}_50_bins_intensity'] = (channel_data['hist'], bin_edges)
            
        intensity_distribution['mean_intensity'] = grey_stats['sum'] / grey_stats['count']
        intensity_distribution['std_intensity'] = np.sqrt(grey_stats['sum_sq'] / grey_stats['count'] - intensity_distribution['mean_intensity']**2)
        intensity_distribution['min_intensity'] = grey_stats['min']
        intensity_distribution['max_intensity'] = grey_stats['max']
        intensity_distribution['percentiles'] = self._hist_percentiles(grey_stats['hist'], bin_edges)
        intensity_distribution['50_bins_intensity'] = (grey_stats['hist'], bin_edges)

        return intensity_distribution


    
    def compute_indices(self, bins: int = 50, n_display: int = 5):
        '''
        Compute RGB-based vegetation/water proxy indices over sampled images.

        For each index:
        - accumulates a dataset-level histogram,
        - computes summary statistics from finite values,
        - stores up to n_display full index maps for randomly selected sample images.

        Args:
            bins (int): Number of histogram bins per index.
            n_display (int): Maximum number of sample index maps to keep per index.

        Returns:
            dict: {
                "histograms": dict[str, tuple[np.ndarray, np.ndarray]],
                    # index_name -> (counts, bin_edges)
                    # counts shape: (bins,), dtype int64
                    # bin_edges shape: (bins + 1,), dtype float32

                "summary": dict[str, dict[str, float | int | np.ndarray]],
                    # index_name -> {
                    #   "mean": float,
                    #   "std": float,
                    #   "min": float,
                    #   "max": float,
                    #   "count": int,
                    #   "percentiles": np.ndarray  # from _hist_percentiles(...), shape (5,)
                    # }

                "sample_maps": dict[str, list[np.ndarray]],
                    # index_name -> up to n_display 2D index arrays (one per selected image)
            }

        Notes:
            - Most indices use fixed histogram edges in [-1, 1].
            - EXG uses robust dynamic edges estimated from a pilot subset.
            - Only finite index values are included in histograms and summary statistics.
        '''

        index_names = ['rgbvi', 'vari', 'exg', 'gli', 'mgrvi', 'bri', 'bgi']
        
        if len(self.sample_indices) == 0:
            self.logger.warning("No sampled images available for index analysis.")
            return {'histograms': {}, 'summary': {}, 'sample_maps': {}, 'sample_ids': []}
        
        n_display = min(n_display, len(self.sample_indices))
        display_indices = set(np.random.choice(self.sample_indices, n_display, replace=False).tolist())
        
        bounded_names = {'rgbvi', 'vari', 'gli', 'mgrvi', 'bri', 'bgi'} # excluding exg for now because it is not naturally bounded to [-1, 1]
        exg_name = 'exg'
        bounded_edges = np.linspace(-1.0, 1.0, bins + 1, dtype=np.float32)

        exg_vals_for_range = []
        pilot_cap = min(len(self.sample_indices), max(25, int(0.2 * len(self.sample_indices))))
        for i in self.sample_indices[:pilot_cap]:
            image = self.load_image(i)
            if image is None or not self.valid_image(image):
                continue
            B, G, R = self.index_analysis._image_to_norm_BGR_float(image)
            exg_map = self.index_analysis.exg(B, G, R)
            vals = exg_map[np.isfinite(exg_map)]
            if vals.size:
                exg_vals_for_range.append(vals)
        if exg_vals_for_range:
            exg_concat = np.concatenate(exg_vals_for_range)
            lo, hi = np.percentile(exg_concat, [1, 99])
            if not np.isfinite(lo) or not np.isfinite(hi) or lo >= hi:
                lo, hi = -1.0, 1.0
        else:
            lo, hi = -1.0, 1.0
        exg_edges = np.linspace(lo, hi, bins + 1, dtype=np.float32)
        edges_by_name = {name: bounded_edges for name in bounded_names}
        edges_by_name[exg_name] = exg_edges
        histograms = {name: np.zeros(bins, dtype=np.int64) for name in index_names}
        summary_acc = {
            name: {"sum": 0.0, "sum_sq": 0.0, "count": 0, "min": np.inf, "max": -np.inf}
            for name in index_names
        }
        sample_maps = {name: [] for name in index_names}
        for i in self.sample_indices:
            image = self.load_image(i)
            if image is None or not self.valid_image(image):
                self.logger.warning(f"Image {i} is not a valid 3-channel image. Skipping.")
                continue
            B, G, R = self.index_analysis._image_to_norm_BGR_float(image)
            idx_maps = {
                "rgbvi": self.index_analysis.rgbvi(B, G, R),
                "vari": self.index_analysis.vari(B, G, R),
                "exg": self.index_analysis.exg(B, G, R),
                "gli": self.index_analysis.gli(B, G, R),
                "mgrvi": self.index_analysis.mgrvi(B, G, R),
                "bri": self.index_analysis.bri(B, G, R),
                "bgi": self.index_analysis.bgi(B, G, R),
            }
            for name, arr in idx_maps.items():
                vals = arr[np.isfinite(arr)]
                if vals.size == 0:
                    continue
                edges = edges_by_name[name]
                histograms[name] += np.histogram(vals, bins=edges)[0]
                acc = summary_acc[name]
                acc["sum"] += float(vals.sum())
                acc["sum_sq"] += float(np.square(vals, dtype=np.float64).sum())
                acc["count"] += int(vals.size)
                acc["min"] = min(acc["min"], float(vals.min()))
                acc["max"] = max(acc["max"], float(vals.max()))
                if i in display_indices and len(sample_maps[name]) < n_display:
                    sample_maps[name].append(arr)
        summary = {}
        for name, acc in summary_acc.items():
            if acc["count"] == 0:
                continue
            mean = acc["sum"] / acc["count"]
            var = max(0.0, acc["sum_sq"] / acc["count"] - mean**2)
            percentiles = self._hist_percentiles(histograms[name], edges_by_name[name])
            summary[name] = {
                "mean": mean,
                "std": float(np.sqrt(var)),
                "min": acc["min"],
                "max": acc["max"],
                "count": acc["count"],
                "percentiles": percentiles,
            }
        hist_out = {name: (histograms[name], edges_by_name[name]) for name in index_names}
        return {
            "histograms": hist_out,
            "summary": summary,
            "sample_maps": sample_maps
        }

    
    def compute_average_image(self):
        '''
        Computes the pixel-wise average image over the dataset (or a random sample).
        Average image that results will also be used to display the colour and intensity heatmaps,
        as well as to compute/display the contrast heatmap. 
        
        Reference shape is the max height and width of the shapes in the dataset.
        If the shapes are not the same, the images are padded to the reference shape.

        Returns: 
        avg_image : np.ndarray (average image) 
        '''
        if len(self.image_paths) == 0:
            self.logger.warning("No image files detected. Unable to compute average image. Returning None.")
            return None
        
        ref_shape = getattr(self, 'ref_shape', (0,0))
        if ref_shape == (0,0):
            self.logger.warning("No reference shape found. Unable to compute average image. Returning None.")
            return None

        image_stack = np.zeros((self.ref_shape[0], self.ref_shape[1], 3), dtype=np.float32)
        valid_count = 0

        for i in self.sample_indices:
            image = self.load_image(i)
            if self.valid_image(image):
                if self.only_one_shape:           
                    image_stack += image
                    valid_count += 1
                else:
                    img_size = image.shape[:2]
                    h_pad = self.ref_shape[0] - img_size[0]
                    w_pad = self.ref_shape[1] - img_size[1]
                    h_pad_top = h_pad//2
                    h_pad_bottom = h_pad - h_pad_top
                    w_pad_left = w_pad//2
                    w_pad_right = w_pad - w_pad_left
                    padding = ((h_pad_top, h_pad_bottom), (w_pad_left, w_pad_right), (0, 0)) # 0, 0 is for the channels
                    image = np.pad(image, padding, mode='constant', constant_values=0)
                    image_stack += image
                    valid_count += 1

            else:
                self.logger.warning(f"Image {i} is not a valid image. Skipping.")
                continue
    
        if valid_count == 0:
            self.logger.warning("No valid images found. Unable to compute average image. Returning None.")
            return None
        elif valid_count < len(self.sample_indices):
            self.logger.warning("Some images were invalid. Average image comprised of only these images.")

        avg_image = (image_stack / valid_count).astype(np.uint8)

        return avg_image

    def compute_contrast_heatmap(self, image, num_windows=25):
        '''
        Computes and returns a windowed grid of average local contrast values over the dataset.
        Each cell in the grid is the mean local contrast in that window.

        Args:
        image: np.ndarray from compute_average_image
        num_windows: int, number of windows to divide the image into. This is the number of rows and columns. Default is 25 for a total of 625 windows.
        Returns:
        contrast_map: np.ndarray of shape (num_windows, num_windows)

        NOTE: Rather than number of windows, we could use the window size to force square windows.
        NOTE: We can consider adding kernel as an argument so that different tasks can be achieved (e.g. edge detection)
        '''
        if not self.valid_image(image):
            self.logger.warning("Invalid image. Unable to compute contrast heatmap. Returning None.")
            return None
    
        grey = cv2.cvtColor(image.astype(np.float32, copy=False), cv2.COLOR_BGR2GRAY)
        h, w = grey.shape
        grid_h = h // num_windows
        grid_w = w // num_windows

        contrast_map = np.zeros((num_windows, num_windows))

        for i in range(num_windows):
            for j in range(num_windows):
                x1, y1 = j * grid_w, i * grid_h
                x2, y2 = (j + 1) * grid_w, (i + 1) * grid_h
                window = grey[y1:y2, x1:x2]

                if window.size > 0:
                    contrast_map[i, j] = window.std() 
                else:
                    contrast_map[i, j] = 0.0

        return contrast_map    


