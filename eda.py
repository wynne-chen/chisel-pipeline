    def get_mask_unique_values(self, mask):
        """
        Returns unique values for a single mask. Return value should be either a list of integers or colours. 
        Returns empty list if mask shape is unexpected.
        """
        if mask.ndim == 2:
            # Standard indexed mask
            return np.unique(mask).tolist()
        elif mask.ndim == 3 and mask.shape[2] == 3:
            pixels = mask.reshape(-1, 3)
            return list(map(tuple, np.unique(pixels, axis=0)))
        elif mask.ndim == 3 and mask.shape[0] == 1:
            mask = np.squeeze(mask)
            return np.unique(mask).tolist()
        else:
            self.logger.warning(f"Unexpected mask shape: {mask.shape}")
            return []

    def all_unique_mask_values(self):
        '''
        Finds all unique values in a sample of mask images and identifies whether it is a single or multiclass dataset.
        Returns:
        global_uniques: set of unique values
        '''
        global_uniques = set()
        for i in self.sample_indices:
            mask = self.load_mask(i)
            uniques = self.get_mask_unique_values(mask)
            if uniques is None:
                continue
            global_uniques.update(uniques)
        # Remove background for class count check
        foreground_uniques = [
            u for u in global_uniques
            if not (
                u == 0 or
                (isinstance(u, (tuple, list, np.ndarray)) and all(v == 0 for v in u))
            )
        ]
        if len(foreground_uniques) == 1:
            self.only_one_class = True
            self.logger.info("This is a single class dataset")
        else:
            self.only_one_class = False
            self.logger.info("This is a multiclass dataset.")
        self.classes = sorted(global_uniques, key=lambda x: (x if isinstance(x, int) else x[0]))
        return global_uniques


    def _compute_single_mask_stats(self, mask, polygons):
        '''
        Computes statistics for a single mask.
        '''
        if mask is None:
            return None
        h, w = mask.shape[:2]
        image_area = h * w
        cx_img, cy_img = (w / 2, h / 2)
        max_dist = np.sqrt(cx_img**2 + cy_img**2)

        polygon_areas = []
        polygon_centroids = []
        centre_bias = []
        edge_touches = 0
        for polygon in polygons:
            if len(polygon) < 6:
                continue
            polygon_np = np.array(polygon, dtype=np.int32).reshape(-1, 1, 2)
            area = cv2.contourArea(polygon_np)
            
            if area == 0:
                continue

            polygon_areas.append(area)

            M = cv2.moments(polygon_np)
            if M['m00'] == 0:
                self.logger.warning("Moment is 0. Skipping centroid calculation.")
                continue
            
            cX = M['m10'] / M['m00']
            cY = M['m01'] / M['m00']
            

            polygon_centroids.append((cX, cY))
            centre_dist = np.sqrt((cX - cx_img)**2 + (cY - cy_img)**2)
            centre_bias.append(centre_dist / max_dist)

            pts = polygon_np.reshape(-1, 2)

            if (
                (pts[:, 0] == 0).any() or
                (pts[:, 1] == 0).any() or
                (pts[:, 0] == w - 1).any() or
                (pts[:, 1] == h - 1).any()
            ):
                edge_touches += 1
        
        total_mask_area = sum(polygon_areas)
            
        return {
            "areas": polygon_areas,
            "centroids": polygon_centroids,
            "counts": len(polygon_areas),
            "total_areas": total_mask_area,
            "area_ratios": total_mask_area / image_area if image_area else 0,
            "centre_bias": centre_bias,
            "edge_touching": edge_touches
        }
    
    def _compute_heatmap(self, centroids):
        '''
        Computes a heatmap of the centroids.
        '''
        if not self.only_one_shape:
            self.logger.info("Cannot compute centroid heatmap: mask tiles have different shapes. Returning None for centroid_heatmap.")
            return None
        heatmap = np.zeros(self.ref_shape, dtype=np.float32)

        for sublist in centroids:
            for cX, cY in sublist:
                ix, iy = int(round(cX)), int(round(cY))
                if 0 <= iy < heatmap.shape[0] and 0 <= ix < heatmap.shape[1]:
                    heatmap[iy, ix] += 1
        return heatmap
    
    def compute_object_stats(self, filtered_masks=None):
        '''
        Computes and returns statistics about mask objects.

        Returns:
            dict with the following keys:
                'areas': list of lists, each sublist contains the areas of objects in an image/tile. Allows you to see how large each average object is. Contrast with 'total_areas'.
                'centroids': list of lists, each sublist contains (x, y) tuples for object centroids in an image/tile
                'counts': list of ints, number of objects per image/tile
                'total_areas': list of floats, total mask area per image/tile. Allows you to see how much of each image is covered by objects. Contrast with 'areas'.
                'area_ratios': list of floats, ratio of total mask area to image area per image/tile
                'centre_bias': list of lists, each sublist contains the normalized distance of each centroid from the image center
                'edge_touching': list of ints, number of objects touching the image edge per image/tile
                'centroid_heatmap': np.ndarray or None, heatmap of centroid locations if all shapes are the same, else None
                'image_shapes': list of tuples, each tuple contains the shape of the correspinding image/mask
        '''
        
        geometry_stats = {
            'areas'        : [],
            'centroids'    : [],
            'counts'       : [],
            'total_areas'  : [],
            'area_ratios'  : [],
            'centre_bias'  : [],
            'edge_touching': [], 
            'image_shapes': []
        }

        if filtered_masks is None:
            masks_iter = (self.load_mask(i) for i in self.sample_indices)
        else:
            masks_iter = iter(filtered_masks)

        for mask in masks_iter:
            polygons = self.annotation_generator.mask_to_polygons(mask)
            result = self._compute_single_mask_stats(mask, polygons)
            if result is None:
                continue
            
            geometry_stats["areas"].append(result["areas"])
            geometry_stats["centroids"].append(result["centroids"])
            geometry_stats["counts"].append(result["counts"])
            geometry_stats["total_areas"].append(result["total_areas"])
            geometry_stats["area_ratios"].append(result["area_ratios"])
            geometry_stats["centre_bias"].append(result["centre_bias"])
            geometry_stats["edge_touching"].append(result["edge_touching"])
            geometry_stats["image_shapes"].append(mask.shape[:2])
        
        geometry_stats["centroid_heatmap"] = self._compute_heatmap(geometry_stats["centroids"])
        
        return geometry_stats
    

